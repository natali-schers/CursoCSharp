// Estrutura condicional if simples 
int num1 = 45;

if (num1 >= 44)
{
    Console.WriteLine("True");
}

// Estrutura condicional if composta
int num2 = 394;

if (num2 == 990)
{
    Console.WriteLine("False");
} else
{
    Console.WriteLine("True");
}

// Estrutura condicional if composta com encadeamentos
int num3 = 6;

if (num3 < 1)
{
    Console.WriteLine("False");
}
else if (num3 == 6)
{
    Console.WriteLine("True");
}
else if (num3 == 0)
{
    Console.WriteLine("False");
}
else
{
    Console.WriteLine("False");
}